

class OutputPrinter:
    @staticmethod
    def welcome():
        print("Welcome to Go-Jek parking lot.")

    @staticmethod
    def end():
        print("Thanks for using Go-Jek parking service")

    @staticmethod
    def notFound():
        print("Not Found")

    @staticmethod
    def statusHeader():
        print("Slot No.   Registration No  Color")

    @staticmethod
    def parkingLotFull():
        print("Sorry, parking lot is full")

    @staticmethod
    def parkingLotEmpty():
        print("Parking lot is empty")

    @staticmethod
    def invalidFile():
        print("Invalid file given")

    @staticmethod
    def printStatement(msg):
        print(msg)





